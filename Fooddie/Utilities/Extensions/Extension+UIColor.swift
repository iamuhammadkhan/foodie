import UIKit

extension UIColor {
    static let themePrimary     = UIColor(named: "ThemeColor") ?? .green
    static let themeSecondary   = UIColor(named: "SecondaryColor") ?? .brown
    static let themeBlack       = UIColor(named: "BlackColor") ?? .black
    static let themeRed         = UIColor.red
    static let themeGreen       = UIColor.green
    static let themeGrey        = UIColor(named: "Gray") ?? .gray
}
